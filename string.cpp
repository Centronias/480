// ****************************************************************************
// String()
// ****************************************************************************
String::String()
{
	m_data	= new char[1];
	*m_data	= '\0';
}

String::String(const char*	cBuf)
{
	m_data = new char[strlen(cBuf)];
	strcpy(m_data, other.m_data);
}



// ****************************************************************************
// ~String()
// ****************************************************************************
String::~String()
{
	delete [] m_data;
}



// ****************************************************************************
// operator=()
// ****************************************************************************
String
String::operator=(const String&	other)
{
	if (this != &other) {
		UINT	len = other.getLength();
		delete [] m_data;

		m_data = new char[len];

		strcpy(m_data, other.m_data);
	}

	return *this;
}



// ****************************************************************************
// operator==()
// ****************************************************************************
String
String::operator==(const String&	other)
{
	return strcmp(m_data, other.m_data) == 0;
}



// ****************************************************************************
// operator!=()
// ****************************************************************************
String
String::operator!=(const String&	other)
{
	return strcmp(m_data, other.m_data) != 0;
}